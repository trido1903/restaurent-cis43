    public void printInformation(){
        String name = containInformation.printInformation().getName();
        String phone = containInformation.printInformation().getPhoneNumber();
        String deliveryAddress = containInformation.printInformation().getAddress();
        String billingAddress = containInformation.printInformation().getBillingAddress();
        if(deliveryAddress == null){
           receiptArea.append("\n\nName: " + name + "\nPhone: " +phone);
        }else{
            receiptArea.append("\n\nName: " + name + "\nPhone: " +phone
                                + "\nAddress: " + deliveryAddress
                                + "\nBilling Address: " + billingAddress);
        }
    }
    
    public void changeToAnotherWindow(){
        // pass arraylist back to the menu
        String subtotal_String = subtotal2.getText().replace("$", ""); 
        double subtotal = Double.parseDouble(subtotal_String);
        Menu menu = new Menu(items, subtotal);
        this.containInformation.clearInformation();
        this.order.clearData();
        menu.setVisible(true);
        this.setVisible(false);
    }
    
    
