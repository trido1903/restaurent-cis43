    private void confirmBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmBtnActionPerformed
        String customer;
        String phoneNumber;
        
        // Initialize the error fields to null
        errorName1.setText(null);
        errorPhone1.setText(null);
        
        errorName2.setText(null);
        errorPhone2.setText(null);
        errorDelivery.setText(null);
        errorBilling2.setText(null);
        
        // variable to control validation
        boolean checkValidation = true;
        
        // If Pickup is selected
        if(radioPickup.isSelected()){
            
            customer = customerField1.getText();
            phoneNumber = phoneField1.getText();
            
            // Check if user enter the fields
            if(customer.length() < 1 ){
                errorName1.setText("*");
                checkValidation = false;
            }
            if(phoneNumber.length() < 1){
                errorPhone1.setText("*");
                checkValidation = false;
            }
            if(checkValidation == true){
                containInformation.addInformation(new Information(customer, phoneNumber, total));
                
                receiptArea.append("\n\t\t\t\t\tTotal: " + total1.getText());
                printInformation();
                setDisableBtn();
            }else{
                JOptionPane.showMessageDialog(new JFrame(), "Please fill out the inputs", "Error", JOptionPane.ERROR_MESSAGE);
               
            } 
        }
        
        
        // If Delivery is selected
        if(radioDelivery.isSelected()){
            customer = customerField2.getText();
            phoneNumber = phoneField2.getText();            
            String deliveryAddress = deliveryField.getText();
            String billing = billingField.getText();
            
            if(customer.length() < 1 ){
                errorName2.setText("*");
                checkValidation = false;
            }
            if(phoneNumber.length() < 1){
                errorPhone2.setText("*");
                checkValidation = false;
            }
            if(deliveryAddress.length() < 1){
                errorDelivery.setText("*");
                checkValidation = false;
            }
            if(!addressCheckbox.isSelected()){
                if(billing.length() < 1){
                    errorBilling2.setText("*");
                    checkValidation = false;
                }
            }
            if(checkValidation == true){
                containInformation.addInformation(new Information(customer, phoneNumber, total, deliveryAddress, billing));
                
                receiptArea.append("\n\t\t\t\t\tTotal: " + total2.getText());
                printInformation();
                setDisableBtn();
            }else{
                JOptionPane.showMessageDialog(new JFrame(), "Please fill out the inputs", "Error", JOptionPane.ERROR_MESSAGE);
               
            }            
        }

    }//GEN-LAST:event_confirmBtnActionPerformed

