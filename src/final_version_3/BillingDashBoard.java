    private void addressCheckboxStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_addressCheckboxStateChanged
         addressCheckbox.addItemListener(new ItemListener() {
             @Override
             public void itemStateChanged(ItemEvent e) {
                 if(e.getStateChange() == ItemEvent.SELECTED){
                    String deliveryAddress = deliveryField.getText();
                    billingField.setText(deliveryAddress);
                    billingField.setEditable(false);
                 }else{
                    billingField.setText(null);
                    billingField.setEditable(true); 
                 }
             }
         });
    }//GEN-LAST:event_addressCheckboxStateChanged

