    private void calculateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateBtnActionPerformed
        sub_total = 0;
        for(int i = 0; i < tblProduct.getRowCount(); i++){
            String totalString = (String.valueOf(tblProduct.getValueAt(i, 2)));
            sub_total += Double.parseDouble(totalString);
        }
        
        double total = sub_total + (sub_total * TAX);
        
        String subTotal_String = df.format(sub_total);
        String total_String = df.format(total);
        
        // Check when click the calculation and user add something to a cart
        if(sub_total > 0){
            subtotalField.setText("$"+(subTotal_String));
            totalField.setText("$"+(total_String));
            checkoutBtn.setEnabled(true);
        }
        //calculateBtn.setEnabled(false);
    }//GEN-LAST:event_calculateBtnActionPerformed

