    // orderObj inheritent from Item class so do not need to create Item Object (this one -> Order -> Item)
    public void saveEveryProduct(int quantity, double price, String name){
        orderObj.setQuantity(quantity);
        orderObj.setPrice(price);
        orderObj.setName(name);  
        double total = orderObj.calTotal();
        total = Double.parseDouble(df.format(total));
        orderObj.addItem(Item.createItem(name, total, quantity));
    }
    
    // remove the row and update the total automatically
    public void removeItem(){
        // cut the character $
        String subtotal_String = subtotalField.getText().replace("$", "");        
        //before subtracting 
        double oldtTotal = Double.parseDouble(subtotal_String);
        
        DefaultTableModel model = (DefaultTableModel) tblProduct.getModel();
        // take the index of the total colum
        int deleteRowIndex = tblProduct.getSelectedRow();
        double totalOfAProduct = 0;
        if(deleteRowIndex >= 0){
            totalOfAProduct = Double.parseDouble(String.valueOf(tblProduct.getValueAt(deleteRowIndex, 2)));
            model.removeRow(deleteRowIndex);
        }
        if(oldtTotal > 0){

            //after subtracting 
            double newTotal = oldtTotal - totalOfAProduct;
            // new total after tax
            double newTotalAfterTax = (newTotal * TAX) + newTotal;
            if(newTotal == 0.0){
                subtotalField.setText("$0.00");
                totalField.setText("$0.00"); 
                checkoutBtn.setEnabled(false);
            }else{
                subtotalField.setText("$" + df.format(newTotal));
                totalField.setText("$" + df.format(newTotalAfterTax));
            }
            
        }
    }
    
    
