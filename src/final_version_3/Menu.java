    // Update the table row when click (Add to a Cart)
    public void addToCart(JTextField textField, String name, double price){

        int quantity = Integer.parseInt(textField.getText());
        double total = quantity * price;
        
        // Add a row to the JTable when click Add a cart
        DefaultTableModel model = (DefaultTableModel) tblProduct.getModel();
        model.addRow(new Object[]{
            name,
            textField.getText(),
            df.format(total)
        });
        calculateBtn.setEnabled(true);
    } 
    
    
    public void checkout(){
        
        // loops every data in jTable and save it Object as placeOrder
        for(int i = 0; i < tblProduct.getRowCount(); i++){
            String name =  (String.valueOf(tblProduct.getValueAt(i, 0)));
            String quantityString =  (String.valueOf(tblProduct.getValueAt(i, 1)));
            String totalString = (String.valueOf(tblProduct.getValueAt(i, 2)));

            int quantity = Integer.parseInt(quantityString);
            double total = Double.parseDouble(totalString);
            
            saveEveryProduct(quantity, total/quantity, name);
        }       
        double subtotal = orderObj.subTotal();
        
        // Close this window and open another window
        ArrayList<Item> items = orderObj.getAllItems();
        BillingDashBoard billingdashboard = new BillingDashBoard(subtotal, orderObj, items);
        billingdashboard.setVisible(true);
        this.setVisible(false);
    }
    
    
    
    
    public void returnBackData(ArrayList<Item> items, double subtotal){
        DefaultTableModel model = (DefaultTableModel) tblProduct.getModel();
        for(int i = 0; i < items.size(); i++){           
            model.addRow(new Object[]{
                items.get(i).getName(),
                items.get(i).getQuantity(),
                items.get(i).getPrice(),
            });
        }
        double total = subtotal + (subtotal * TAX);
        
        String subTotal_String = df.format(subtotal);
        String total_String = df.format(total);
        subtotalField.setText("$"+(subTotal_String));
        totalField.setText("$"+(total_String));
    }
